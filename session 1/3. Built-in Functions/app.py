# Built-in functions:
# print: A built-in function that prints one or more objects to the console or 
# standard output. This function is commonly used for debugging and outputting 
# information to the user.
#
# len: A built-in function that returns the length of an object, such as a 
# string, list, or tuple. This function is commonly used for checking the size 
# of data structures.
#
# type: A built-in function that returns the type of an object. This function 
# is commonly used for checking the data type of variables and values.
#
# range: A built-in function that generates a sequence of integers, given a 
# starting value, an ending value, and an optional step size. This function is 
# commonly used for looping and generating ranges of numbers.
#
# open: A built-in function that opens a file and returns a file object, which 
# can be used to read or write to the file. This function is commonly used for 
# reading and writing files in Python.
#
# str: A built-in function that converts an object to a string representation. 
# This function is commonly used for converting data to a format that can be 
# printed or displayed to the user.
#
# int: A built-in function that converts a string or float to an integer. 
# This function is commonly used for converting input values to the 
# correct data type.
#
# list: A built-in function that converts an object to a list. This function is 
# commonly used for converting data structures to lists, which can be 
# manipulated more easily.
#

# Functions & Methods
#
# Function: A function is a block of code that performs a specific task and 
# can be called from other parts of the program. Functions can accept input 
# arguments and return output values. Functions in Python are defined using the 
# def keyword, followed by the function name and any input parameters.
#
# Method: A method is a function that is associated with a specific object 
# or class. In Python, methods are called using the dot notation, with the 
# object or class instance preceding the method name. Methods can access and 
# modify the data associated with the object or class instance.



# Parameters & Arguments
#
# Parameter: A variable in a function or method definition that represents a value 
# or a reference passed to the function or method when it is called.
#
# Argument: The actual value or reference that is passed to the function or method 
# when it is called, corresponding to a parameter in the function or method definition.
#
# In simpler terms, a parameter is like a placeholder that tells the function or method 
# what kind of data it should expect, while an argument is the actual value that is passed 
# into the function or method to replace that placeholder.
#

def add_numbers(a, b):
    return a + b

add_numbers(1, 2)

