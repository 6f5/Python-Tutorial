# Arithmetic Operators
# +  Addition - Sum of two operands
# -  Subtraction - Left operand minus right operand
# *  Multiplication
# /  Division
# ** Exponentiation - Left operand raised to the power of right operand
# // Floor Division - Equivalent to math.floor(a / b)
# %  Modulus - Remainder of a/b

# Assignment Operators
# =
# +=
# -=
# *=
# /=
# //=
# %=

# Comparison Operators
# > - True if the left operand is higher than the right one
# < - True if the left operand is lower than right one
# == - True if the operands are equal
# != - True if the operands are not equal
# >= - True if the left operand is higher thant or equal to the right one
# <= - True if the left operand is lower than or equal to the right one

# Logical Operators
# and  - True if both are true
# or   - True if at least one is true
# not  - Returns True if an expression evaluates to false and vice-versa

# Identity Operators
# is     - True if both are true
# is not - True if at least one is true

# Membership Test Operators
# in - Returns True if the sequence contains the sepcified item else returns False
# not in - Returns True if the sequence does not contains the sepcified item, else
# returns False

# Bitwise Operators
# &   - Sets each bit to 1 if both bits are 1.
# |   - Sets each bit to 1 if one of two bits is 1.
# ^   - Sets each bit to 1 if only one of two bits is 1.
# ~   - Inverts all the bits.
# <<  - Shift left by pushing zeros in from the right and let the leftmost bits fall off.
# >>  - Shift right by pushing copies of the leftmost bit in from the left, and let the
# rightmost bits fall off